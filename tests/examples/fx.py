from math import sin, cos, tan, asin, acos, atan, log, sqrt

def fsin(x):
    z = sin(x)
    return z

def fcos(x):
    z = cos(x)
    return z

def ftan(x):
    z = tan(x)
    return z

def fsqrt(x):
    z = sqrt(x)
    return z

def flog(x):
    z = log(x)
    return z

def fasin(x):
    z = asin(x)
    return z

def fatan(x):
    z = atan(x)
    return z
